        #region --------------------------辅助方法--------------------------

        /// <summary>
        /// 构造redis的Key值
        /// </summary>
        /// <param name="oldKey">传递的key</param>
        /// <returns></returns>
        private string AddSysCustomKey(string oldKey)
        {
            var prefixKey = CustomKey ?? RedisConnectionHelp.SysCustomKey;
            return prefixKey + oldKey;
        }

        /// <summary>
        /// 连接redis并且返回redis连接之后的对象
        /// </summary>
        /// <typeparam name="T">Db对象</typeparam>
        /// <param name="func">func函数</param>
        /// <returns></returns>
        private T Connection<T>(Func<IDatabase, T> func)
        {
            var database = _conn.GetDatabase(DbNum);
            return func(database);
        }

        /// <summary>
        /// 将RedisValue对象转换为实体对象
        /// </summary>
        /// <typeparam name="T">转换后的实体</typeparam>
        /// <param name="value">转换前的对象</param>
        /// <returns></returns>
        private T ConvertObj<T>(RedisValue value)
        {
            return JsonConvert.DeserializeObject<T>(value);
        }

        /// <summary>
        /// 将RedisValue数组对象转换为实体集合对象
        /// </summary>
        /// <typeparam name="T">转换后的实体</typeparam>
        /// <param name="value">转换前的对象</param>
        /// <returns></returns>
        private List<T> ConvetList<T>(RedisValue[] values)
        {
            List<T> result = new List<T>();
            foreach (var item in values)
            {
                var model = ConvertObj<T>(item);
                result.Add(model);
            }
            return result;
        }

        /// <summary>
        /// 将List集合的redisKey集合转换为RedisKey[]数组对象
        /// </summary>
        /// <param name="redisKeys">redisKey集合</param>
        /// <returns></returns>
        private RedisKey[] ConvertRedisKeys(List<string> redisKeys)
        {
            return redisKeys.Select(redisKey => (RedisKey)redisKey).ToArray();
        }

