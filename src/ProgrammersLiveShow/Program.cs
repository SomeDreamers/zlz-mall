using System.IO;
using Microsoft.AspNetCore.Hosting;

namespace ProgrammersLiveShow
{
    /// <summary>
    /// 系统初始化信息
    /// </summary>
    public class Program
    {
        public static void Main(string[] args)
        {
            var host = new WebHostBuilder()
                .UseKestrel()
                .UseContentRoot(Directory.GetCurrentDirectory())
                .UseIISIntegration()
                .UseStartup<Startup>()
                .Build();

            host.Run();
        }
    }
}
